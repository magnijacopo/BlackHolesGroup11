package it.polimi.group11.enumeration;


/**
 * Every hole of the grid can be cover with a bead, and every bead has an owner.
 * Specify the possible owner of a bead.
 */

public enum Owner {

    /**
     * There is no bead on that hole, so there is no owner.
     */
    NONE,

    /**
     * The owner of the bead is Player One.
     */
    PLAYERONE,

    /**
     * The owner of the bead is Player Two.
     */
    PLAYERTWO,

    /**
     * The owner of the bead is Player Three.
     */
    PLAYERTHREE,

    /**
     * The owner of the bead is Player Four.
     */
    PLAYERFOUR;



}


